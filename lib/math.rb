def addition(num1, num2)
sum = num1 + num2
return sum
end

def subtraction(num1, num2)
diff = num1 - num2
return diff
end

def division(num1, num2)
quot = num1 / num2
return quot
end

def multiplication(num1, num2)
product = num1*num2
return product
end

def modulo(num1, num2)
remain = num1%num2
return remain
end

def square_root(num)
root = Math.sqrt(num)
return root
end
